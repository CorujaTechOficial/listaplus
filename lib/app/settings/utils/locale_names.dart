String getLocaleDisplayName(String? localeCode, AppLocalizations l10n) {
  if (localeCode == null || localeCode.isEmpty) {
    return l10n.languageSystem;
  }
  
  final normalized = localeCode.replaceAll('-', '_');
  final parts = normalized.split('_');
  final langCode = parts[0];
  final countryCode = parts.length > 1 ? parts[1] : null;

  final nativeName = _nativeLanguageNames[langCode] ?? langCode;
  if (countryCode != null) {
    if (langCode == 'zh') {
      if (countryCode == 'CN') {
        return '简体中文 (中国)';
      }
      if (countryCode == 'HK') {
        return '繁體中文 (香港)';
      }
      if (countryCode == 'TW') {
        return '繁體中文 (台灣)';
      }
    }
    final region = _regionalNames[countryCode] ?? countryCode;
    return '$nativeName ($region)';
  }
  return nativeName;
}

String getEnglishLanguageName(String langCode) {
  return _englishLanguageNames[langCode] ?? langCode;
}

String getNativeLanguageName(String langCode) {
  return _nativeLanguageNames[langCode] ?? langCode;
}
